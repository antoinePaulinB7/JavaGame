package ecouteurs;

import java.util.EventListener;
/**
 * Ecouteur des informations du joueur 1
 * @author Mihai
 *
 */
public interface MondeListenerInfosJ1 extends EventListener {
	/**
	 * Detecte les changements d'experience du joueur 1
	 * @param niveau Le niveau
	 * @param experience L'experience
	 * @param pointsDeVie Les points de vie
	 */
	public void experienceAjouteJ1(int niveau, double experience, double pointsDeVie);
	/**
	 * Detecte les changements du score
	 * @param score Le score
	 */
	public void scoreAugmente(long score);
	/**
	 * Detecte les changements des points de vie du joueur 1
	 * @param pointsDeVie Les points de vie
	 */
	public void updateVieJ1(double pointsDeVie);

}
